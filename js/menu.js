function clickMenu() {
    if (itens.style.display == 'flex') {
        itens.style.display = 'none';
    } else {
        itens.style.display = 'flex';
    }
}

function clickMobile() {
    if (itensmobile.style.display == 'flex') {
        itensmobile.style.display = 'none';
    } else {
        itensmobile.style.display = 'flex';
    }
}
